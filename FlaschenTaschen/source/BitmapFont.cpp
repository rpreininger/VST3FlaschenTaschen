static const int FONT_FIRST_CHAR = 32;  // Space
static const int FONT_LAST_CHAR = 126;  // ~
static const int FONT_CHAR_COUNT = FONT_LAST_CHAR - FONT_FIRST_CHAR + 1;

//------------------------------------------------------------------------
// BitmapFont Implementation
//------------------------------------------------------------------------
int BitmapFont::charToIndex(char c) {
    if (c < FONT_FIRST_CHAR || c > FONT_LAST_CHAR) {
        return 0;  // Return space for unknown characters
    }
    return c - FONT_FIRST_CHAR;
}

const uint8_t* BitmapFont::getCharBitmap(char c) {
    return FONT_DATA[charToIndex(c)];
}

int BitmapFont::getTextWidth(const std::string& text) const {
    if (text.empty()) return 0;
    int width = static_cast<int>(text.length()) * getScaledCharWidth();
    width += (static_cast<int>(text.length()) - 1) * getScaledSpacing();
    return width;
}

void BitmapFont::renderChar(FlaschenTaschenClient& client, char c, int x, int y,
                            const Color& color, const Color& bgColor) const {
    const uint8_t* bitmap = getCharBitmap(c);

    for (int row = 0; row < CHAR_HEIGHT; ++row) {
        uint8_t rowData = bitmap[row];
        for (int col = 0; col < CHAR_WIDTH; ++col) {
            bool pixelOn = (rowData & (1 << col)) != 0;
            const Color& pixelColor = pixelOn ? color : bgColor;

            // Draw scaled pixel
            for (int sy = 0; sy < scale_; ++sy) {
                for (int sx = 0; sx < scale_; ++sx) {
                    client.setPixel(x + col * scale_ + sx,
                                    y + row * scale_ + sy,
                                    pixelColor);
                }
            }
        }
    }
}

void BitmapFont::renderText(FlaschenTaschenClient& client, const std::string& text,
                            int x, int y, const Color& color, const Color& bgColor) const {
    int cursorX = x;
    for (char c : text) {
        renderChar(client, c, cursorX, y, color, bgColor);
        cursorX += getScaledCharWidth() + getScaledSpacing();
    }
}

void BitmapFont::renderTextCentered(FlaschenTaschenClient& client, const std::string& text,
                                    int y, const Color& color, const Color& bgColor) const {
    int textWidth = getTextWidth(text);
    int x = (client.getWidth() - textWidth) / 2;
    renderText(client, text, x, y, color, bgColor);
}

void BitmapFont::renderTextCenteredFull(FlaschenTaschenClient& client, const std::string& text,
                                        const Color& color, const Color& bgColor) const {
    int textWidth = getTextWidth(text);
    int textHeight = getScaledCharHeight();
    int x = (client.getWidth() - textWidth) / 2;
    int y = (client.getHeight() - textHeight) / 2;
    renderText(client, text, x, y, color, bgColor);
}

